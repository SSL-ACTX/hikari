// If-else statements
if (true) {
  console.log("if-true");
} else {
  console.log("if-false");
}

if (false) {
  console.log("if-true");
} else {
  console.log("if-false");
}

let x = 10;
if (x > 5) {
  console.log("x is greater than 5");
}

// While loops
let i = 0;
while (i < 3) {
  console.log(i);
  i = i + 1;
}
